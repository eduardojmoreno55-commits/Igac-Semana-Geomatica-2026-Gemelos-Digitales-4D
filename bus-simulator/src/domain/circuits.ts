/**
 * Envuelve una polyline real ya cerrada (primer punto == último) en el
 * contrato `Circuit` que consume `CircuitGeometry`.
 * @param id Identificador estable en kebab-case.
 * @param name Nombre legible de la zona.
 * @param coordinates Loop cerrado `[lon, lat]`, en calles reales.
 * @returns Circuito con geometría de loop cerrado.
 */
function realLoop(id: string, name: string, coordinates: [number, number][]): Circuit {
  return {
    id,
    name,
    line: {
      type: "Feature",
      properties: { circuitId: id, name },
      geometry: {
        type: "LineString",
        coordinates,
      },
    },
  };
}

/** Catálogo de circuitos asignados a los 4 buses. */
export const CIRCUITS: Circuit[] = [
  realLoop("chapinero", "Circuito Chapinero", CHAPINERO_ROUTE),
  realLoop("candelaria", "Circuito La Candelaria", CANDELARIA_ROUTE),
  realLoop("usaquen", "Circuito Usaquén", USAQUEN_ROUTE),
  realLoop("suba", "Circuito Suba", SUBA_ROUTE),
];

/**
 * Busca un circuito por ID.
 * @param id Identificador del circuito.
 * @returns El circuito coincidente, o `undefined` si no existe.
 */
export function findCircuit(id: string): Circuit | undefined {
  return CIRCUITS.find((circuit) => circuit.id === id);
}
